// ============================================================
// server.js — Main Express Server
// ============================================================
// This is the entry point for the backend.
// It sets up Express, connects middleware, and mounts routes.
// ============================================================

// Load environment variables from .env file (if it exists)
require("dotenv").config();

const express = require("express");
const cors    = require("cors");
const path    = require("path");

// Import our route files
const productRoutes = require("./routes/products");
const ideaRoutes = require("./routes/ideas");

const app  = express();
const PORT = process.env.PORT || 3001;

const isLocalDevOrigin = (origin) => {
  if (!origin) return true;
  try {
    const u = new URL(origin);
    if (u.hostname === "localhost" || u.hostname === "127.0.0.1") return true;
  } catch {
    return false;
  }
  return false;
};

const parseClientOrigins = () => {
  const raw = process.env.CLIENT_ORIGIN;
  if (!raw || !String(raw).trim()) return [];
  return String(raw)
    .split(",")
    .map((s) => s.trim())
    .filter(Boolean);
};

// ---- Middleware ----
app.use(
  cors({
    origin: (origin, callback) => {
      if (!origin || isLocalDevOrigin(origin)) {
        return callback(null, true);
      }
      const allowed = parseClientOrigins();
      if (allowed.length === 0) {
        return callback(null, true);
      }
      if (allowed.includes(origin)) {
        return callback(null, true);
      }
      return callback(null, false);
    }
  })
);
// Parse incoming JSON request bodies
app.use(express.json());

// ---- Static File Serving — Generated Digital Products ----
// Files generated by digitalProductService.js are saved to
// backend/generated-products/ and exposed at GET /downloads/*
// so the frontend can link directly to them for download.
//
// Example: GET /downloads/abc123-1700000000000-budget-planner.csv
//
// express.static streams the file and sets Content-Disposition
// headers automatically so browsers trigger a download.
const generatedProductsDir = path.join(__dirname, "generated-products");
app.use(
  "/downloads",
  express.static(generatedProductsDir, {
    // Tell browsers to download the file rather than open it inline
    setHeaders: (res, filePath) => {
      if (filePath.endsWith(".csv")) {
        res.setHeader("Content-Disposition", `attachment; filename="${path.basename(filePath)}"`);
        res.setHeader("Content-Type", "text/csv; charset=utf-8");
      }
    }
  })
);

// ---- Routes ----
// All product-related routes live under /api/products
app.use("/api/products", productRoutes);
// Ideas / research intake + opportunity scorer
app.use("/api/ideas", ideaRoutes);

// ---- Health Check ----
// Useful to quickly verify the server is running
app.get("/api/health", (req, res) => {
  res.json({ status: "ok", message: "AI E-Commerce HQ backend is running!" });
});

// ---- Start Server ----
app.listen(PORT, "0.0.0.0", () => {
  console.log(`\n🚀 Backend server running at http://localhost:${PORT}`);
  console.log(`   Health check:    http://localhost:${PORT}/api/health`);
  console.log(`   Products API:    http://localhost:${PORT}/api/products`);
  console.log(`   Ideas API:       http://localhost:${PORT}/api/ideas`);
  console.log(`   Downloads:       http://localhost:${PORT}/downloads/\n`);
});
